#include <iostream>
#include <math.h>

using namespace std;

int main(){
    int a=5, b=2;
    int soma=a+b;
    cout << "soma: " << soma << endl;
    
    int sub=a-b;
    cout << "subtaração: " << sub << endl;
    
    float c=5, d=2;
    float divisao=c/d;
    cout << "divisao: " << divisao << endl;
    
    float mult=c*d;
    cout << "multiplicação: " << mult << endl;
    
    float pot=pow(a,b);
    cout << "potencia: " << pot << endl;
}
